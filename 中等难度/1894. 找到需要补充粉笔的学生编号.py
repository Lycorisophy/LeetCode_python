def chalkReplacer(chalk: [int], k: int) -> int:
    n = len(chalk)
    ttl = [0] * n
    ttl[0] = chalk[0]
    for i in range(1, n):
        ttl[i] = ttl[i - 1] + chalk[i]
    more = k % ttl[n - 1]
    L, R = 0, n - 1
    while L <= R:
        if ttl[0] > more:
            return 0
        m = L + (R - L) // 2
        if ttl[m] > more:
            for i in range(m, -1, -1):
                if ttl[i] <= more:
                    break
                m = i
            return m
        elif ttl[m] == more:
            return m + 1
        else:
            L = m + 1


